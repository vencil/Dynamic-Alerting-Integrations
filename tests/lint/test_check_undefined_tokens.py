"""Tests for check_undefined_tokens.py — Detect JSX/CSS references to --da-color-* tokens not defined in design-tokens.css.

Pinned contracts
----------------
1. **Detection**: `var(--da-color-<name>)` reference in JSX/JS/CSS
   where `<name>` is NOT a `--da-color-<name>:` definition in
   design-tokens.css. Captures `<name>`. Allows fallback `,` syntax
   (`var(--da-color-x, fallback)`) — still flags missing `--da-color-x`.
2. **Suppression**:
   - Per-line `# undefined-tokens: ignore` marker (3-line lookback).
   - Tokens defined in light OR dark `:root` block count as defined.
   - Non-color categories (`--da-space-*` / `--da-font-*` / etc) NOT
     in scope (regex anchors on `--da-color-` prefix).
3. **Severity matrix** (`_compute_exit_code`):
   - !ci, * → exit 0
   - ci, 0 findings → exit 0
   - ci, >0 findings → exit 1
4. **Robustness**: empty source / whitespace inside `var()` / no
   `tokens-css` file → exit 2 (misconfig).
5. **Live dogfood** (`TestLiveRepo`): real `docs/**` JSX/CSS yields
   zero findings — gates broken state from landing.

Generated by ``scripts/tools/dx/scaffold_lint.py``. See PR #171.
"""
from __future__ import annotations

import os
import sys
from pathlib import Path

import pytest

_TOOLS_DIR = os.path.join(
    os.path.dirname(__file__), "..", "..", "scripts", "tools", "lint"
)
sys.path.insert(0, _TOOLS_DIR)

import check_undefined_tokens as lint  # noqa: E402


def _scan(source: str, known_set, fake_path: str = "fake.jsx"):
    return lint.scan_source(Path(fake_path), source, known_set=known_set)


# ---------------------------------------------------------------------------
# Detection — positive cases
# ---------------------------------------------------------------------------
class TestDetection:
    def test_empty_source_no_findings(self):
        assert _scan("", {"color-fg"}) == []

    def test_undefined_token_flagged(self):
        src = 'color: var(--da-color-typo);'
        findings = _scan(src, {"color-fg"})
        assert len(findings) == 1
        assert findings[0].token_name == "color-typo"

    def test_defined_token_not_flagged(self):
        src = 'color: var(--da-color-fg);'
        assert _scan(src, {"color-fg"}) == []

    def test_var_with_fallback_still_checks_name(self):
        src = 'color: var(--da-color-typo, #ccc);'
        findings = _scan(src, {"color-fg"})
        assert len(findings) == 1
        assert findings[0].token_name == "color-typo"

    @pytest.mark.parametrize(
        "src",
        [
            'className="text-[color:var(--da-color-mising)]"',
            "background: var(--da-color-mising);",
            "border: 1px solid var(--da-color-mising);",
            "var( --da-color-mising )",
        ],
    )
    def test_various_contexts(self, src):
        findings = _scan(src, {"color-fg"})
        assert len(findings) == 1
        assert findings[0].token_name == "color-mising"

    def test_multiple_findings_in_one_line(self):
        src = "var(--da-color-x); var(--da-color-y);"
        findings = _scan(src, set())
        assert len(findings) == 2

    def test_multiple_findings_across_lines(self):
        src = "var(--da-color-x);\nvar(--da-color-y);\n"
        findings = _scan(src, set())
        assert findings[0].line == 1
        assert findings[1].line == 2

    @pytest.mark.parametrize(
        "src,token",
        [
            ("padding: var(--da-space-99);", "space-99"),
            ("font-size: var(--da-font-zzz);", "font-zzz"),
            ("box-shadow: var(--da-shadow-bogus);", "shadow-bogus"),
            ("border-radius: var(--da-radius-none-but-typo);", "radius-none-but-typo"),
            ("transition: var(--da-transition-stale);", "transition-stale"),
            ("line-height: var(--da-line-bogus);", "line-bogus"),
        ],
    )
    def test_all_categories_flagged_when_undefined(self, src, token):
        # S#86 generalisation: every --da-<category>-<name> ref must
        # resolve, not just `--da-color-`.
        findings = _scan(src, set())
        assert len(findings) == 1
        assert findings[0].token_name == token


