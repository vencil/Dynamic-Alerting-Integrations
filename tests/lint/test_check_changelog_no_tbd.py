class TestMain:
    @pytest.mark.timeout(15)
    def test_main_clean_exit_0(self, tmp_path):
        clean = tmp_path / "CHANGELOG.md"
        clean.write_text("## v1.0.0\n- Real feature\n", encoding="utf-8")
        rc = lint.main(["--ci", str(clean)])
        assert rc == 0

    @pytest.mark.timeout(15)
    def test_main_dirty_under_ci_exit_1(self, tmp_path, capsys):
        dirty = tmp_path / "CHANGELOG.md"
        dirty.write_text(
            "## v1.0.0\n- Feature shipped (TBD).\n",
            encoding="utf-8",
        )
        rc = lint.main(["--ci", str(dirty)])
        assert rc == 1
        captured = capsys.readouterr()
        assert "1 placeholder" in captured.err

    @pytest.mark.timeout(15)
    def test_main_dirty_no_ci_exit_0(self, tmp_path):
        dirty = tmp_path / "CHANGELOG.md"
        dirty.write_text("- TBD entry\n", encoding="utf-8")
        rc = lint.main([str(dirty)])
        assert rc == 0


# ---------------------------------------------------------------------------
# Live dogfood — real CHANGELOG must pass
# ---------------------------------------------------------------------------
class TestLiveRepo:
    @pytest.mark.timeout(30)
    def test_live_repo_has_no_findings(self):
        candidates = [
            lint.PROJECT_ROOT / name for name in lint._DEFAULT_TARGET_FILES
        ]
        candidates = [p for p in candidates if p.is_file()]
        assert candidates, "expected CHANGELOG.md to exist"

        all_findings = []
        for path in candidates:
            source = path.read_text(encoding="utf-8", errors="replace")
            all_findings.extend(lint.scan_source(path, source))

        assert all_findings == [], (
            f"Live CHANGELOG has {len(all_findings)} placeholder(s):\n"
            + "\n".join(f"  - {f.render()}" for f in all_findings[:10])
        )
