"""Tests for check_changelog_no_tbd.py — Detect TBD/TODO placeholders in CHANGELOG (Self-review Gap A.c).

Pinned contracts
----------------
1. **Detection**:
   - Bare ``TBD`` (word boundary, all-caps).
   - Bracketed ``[TBD]`` / ``(TBD)``.
   - HTML placeholders ``<!-- TODO -->`` / ``<!-- TBD -->`` /
     ``<!-- FIXME -->`` / ``<!-- XXX -->`` (case-insensitive
     placeholder text inside the comment).
2. **Suppression**:
   - Per-line ``<!-- changelog-no-tbd: ignore -->`` marker (3-line
     lookback).
   - Inside fenced code blocks ``` ``` ```.
   - Inside backticks (`` `TBD` ``).
   - Lower-case ``tbd`` in prose (NOT flagged).
3. **Severity matrix** (`_compute_exit_code`):
   - !ci, * → exit 0
   - ci, 0 findings → exit 0
   - ci, >0 findings → exit 1
4. **Robustness**: empty source / mixed case / nested forms.
5. **Live dogfood** (`TestLiveRepo`): actual CHANGELOG.md +
   CHANGELOG-archive.md must yield zero findings — gates broken
   state from landing.

Generated by ``scripts/tools/dx/scaffold_lint.py``. See PR #171.
"""
from __future__ import annotations

import os
import sys
from pathlib import Path

import pytest

_TOOLS_DIR = os.path.join(
    os.path.dirname(__file__), "..", "..", "scripts", "tools", "lint"
)
sys.path.insert(0, _TOOLS_DIR)

import check_changelog_no_tbd as lint  # noqa: E402


def _scan(source: str, fake_path: str = "CHANGELOG.md"):
    return lint.scan_source(Path(fake_path), source)


# ---------------------------------------------------------------------------
# Detection — positive cases
# ---------------------------------------------------------------------------
class TestDetection:
    def test_empty_source_no_findings(self):
        assert _scan("") == []

    @pytest.mark.parametrize(
        "src",
        [
            "- Fixed bug: TBD",
            "* commit (TBD) shipped 2026-04-19",
            "ID: [TBD]",
            "<!-- TODO -->",
            "<!-- TBD -->",
            "<!-- FIXME -->",
            "<!-- XXX -->",
            "<!--TODO-->",  # no whitespace
            "<!-- todo -->",  # case-insensitive HTML comment
        ],
    )
    def test_positive_detection(self, src):
        findings = _scan(src)
        assert len(findings) >= 1, f"expected finding in {src!r}, got {findings}"

    def test_multiple_findings_in_one_line(self):
        src = "TBD foo (TBD) bar"
        assert len(_scan(src)) == 2

    def test_multiple_findings_across_lines(self):
        src = "Line 1: TBD\nLine 2: <!-- TODO -->\n"
        findings = _scan(src)
        assert len(findings) == 2
        assert findings[0].line == 1
        assert findings[1].line == 2


