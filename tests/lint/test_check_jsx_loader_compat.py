# ---------------------------------------------------------------------------
class TestNonAllowlistImportRule:
    @pytest.mark.parametrize(
        "src",
        [
            "import _ from 'lodash';\n",
            "import axios from 'axios';\n",
            "import { format } from 'date-fns';\n",
            "import * as ramda from 'ramda';\n",
            "import X, { Y } from 'some-pkg';\n",
            "import './local.css';\n",  # technically also fails (not handled)
        ],
    )
    def test_non_allowlist_flagged(self, src):
        findings = _scan(src)
        # `import './local.css'` doesn't match _IMPORT_RE because it
        # has no `from` clause; we only flag the `from` form here. So
        # the last param is exempt — let's verify.
        if "from" not in src:
            assert findings == [], (
                f"Plain `import './x'` not in scope (no `from` clause): {src!r}"
            )
        else:
            assert len(findings) == 1
            assert findings[0].rule == "non-allowlist-import"

    @pytest.mark.parametrize(
        "src",
        [
            "import React from 'react';\n",
            "import { useState } from 'react';\n",
            "import { useState, useMemo } from 'react';\n",
            "import React, { useState } from 'react';\n",
            "import { Bell, Cog } from 'lucide-react';\n",
        ],
    )
    def test_allowlist_imports_pass(self, src):
        assert _scan(src) == []


