class TestRequireCallRule:
    def test_bare_require_flagged(self):
        src = "const fs = require('fs');\n"
        findings = _scan(src)
        assert len(findings) == 1
        assert findings[0].rule == "require-call"

    def test_require_with_spaces(self):
        src = "const x = require ('x');\n"
        findings = _scan(src)
        assert len(findings) == 1
        assert findings[0].rule == "require-call"

    def test_require_in_comment_not_flagged(self):
        # JS line comment skipped by _is_comment_line heuristic.
        src = "// const x = require('foo');\n"
        assert _scan(src) == []

    def test_require_in_block_comment_body_skipped(self):
        # `* ...` lines (JSDoc / block comment body) skipped.
        src = " * Example: require('foo')\n"
        assert _scan(src) == []


