# Suppression — ignore comment behavior (3-line lookback)
# ---------------------------------------------------------------------------
class TestSuppression:
    def test_ignore_on_same_line_suppresses(self):
        src = "export const X = 1; // <!-- jsx-loader-compat: ignore -->\n"
        assert _scan(src) == []

    def test_ignore_one_line_above_suppresses(self):
        src = (
            "// <!-- jsx-loader-compat: ignore -->\n"
            "export function helper() {}\n"
        )
        assert _scan(src) == []

    def test_ignore_three_lines_above_suppresses(self):
        src = (
            "// <!-- jsx-loader-compat: ignore -->\n"
            "// rationale: legacy compat shim\n"
            "// remove once X migrates\n"
            "export function helper() {}\n"
        )
        assert _scan(src) == []

    def test_ignore_four_lines_above_does_not_suppress(self):
        src = (
            "// <!-- jsx-loader-compat: ignore -->\n"
            "// l2\n"
            "// l3\n"
            "// l4\n"
            "export function helper() {}\n"
        )
        findings = _scan(src)
        assert len(findings) == 1


