# main() integration
# ---------------------------------------------------------------------------
class TestMain:
    @pytest.mark.timeout(15)
    def test_main_clean_exit_0(self, tmp_path):
        clean = tmp_path / "clean.jsx"
        clean.write_text(
            "import React, { useState } from 'react';\n"
            "import { Bell } from 'lucide-react';\n"
            "function helper() {}\n"
            "export default function Tool() { return null; }\n",
            encoding="utf-8",
        )
        rc = lint.main(["--ci", str(clean)])
        assert rc == 0

    @pytest.mark.timeout(15)
    def test_main_dirty_under_ci_exit_1(self, tmp_path, capsys):
        dirty = tmp_path / "dirty.jsx"
        dirty.write_text(
            "import _ from 'lodash';\n"
            "export function helper() {}\n"
            "export default function Tool() {}\n",
            encoding="utf-8",
        )
        rc = lint.main(["--ci", str(dirty)])
        assert rc == 1
        captured = capsys.readouterr()
        assert "2 jsx-loader-compat" in captured.err
        assert "named-export" in captured.err
        assert "non-allowlist-import" in captured.err

    @pytest.mark.timeout(15)
    def test_main_dirty_no_ci_exit_0(self, tmp_path):
        dirty = tmp_path / "dirty.jsx"
        dirty.write_text("export const X = 1;\n", encoding="utf-8")
        rc = lint.main([str(dirty)])
        assert rc == 0  # report-only mode


