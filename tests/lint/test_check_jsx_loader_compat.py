# Multi-rule on same source
# ---------------------------------------------------------------------------
class TestMultipleRules:
    def test_named_export_plus_require_in_one_file(self):
        src = (
            "import _ from 'lodash';\n"  # rule 2
            "export function helper() {}\n"  # rule 1
            "const fs = require('fs');\n"  # rule 3
        )
        findings = _scan(src)
        rules = sorted(f.rule for f in findings)
        assert rules == ["named-export", "non-allowlist-import", "require-call"]

    def test_findings_sorted_by_line(self):
        src = (
            "export const A = 1;\n"  # line 1, rule 1
            "import x from 'pkg';\n"  # line 2, rule 2
            "const z = require('z');\n"  # line 3, rule 3
        )
        findings = _scan(src)
        assert [f.line for f in findings] == [1, 2, 3]


