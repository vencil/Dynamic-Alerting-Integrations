# Live dogfood — actual repo must pass
# ---------------------------------------------------------------------------
class TestLiveRepo:
    @pytest.mark.timeout(30)
    def test_live_repo_has_no_findings(self):
        import argparse

        ns = argparse.Namespace(paths=[])
        candidates = lint._resolve_target_paths(ns)
        assert candidates, "expected docs/interactive/ jsx files to exist"

        all_findings = []
        for path in candidates:
            try:
                source = path.read_text(encoding="utf-8", errors="replace")
            except OSError:
                continue
            all_findings.extend(lint.scan_source(path, source))

        assert all_findings == [], (
            f"Live repo has {len(all_findings)} jsx-loader-compat violation(s):\n"
            + "\n".join(f"  - {f.render()}" for f in all_findings[:10])
        )
