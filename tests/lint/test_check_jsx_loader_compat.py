"""Tests for check_jsx_loader_compat.py — Detect JSX-loader-incompatible module syntax (named exports / non-allowlist imports / require() calls).

Pinned contracts
----------------
1. **Detection** — three rule classes:
   - `named-export`: `^export (function|const|let|var|class) ...` (NOT default)
   - `non-allowlist-import`: `import ... from 'X'` where X ∉ {react, lucide-react}
   - `require-call`: `\\brequire\\s*\\(` anywhere
2. **Suppression**:
   - `<!-- jsx-loader-compat: ignore -->` per-line marker (3-line lookback)
   - JS line comments (`// ...`) and block-comment bodies (`* ...`)
     skipped (heuristic; multi-line strings not handled but suppressible)
   - Allowed: `import .* from 'react'` and `import { .. } from 'lucide-react'`
   - Allowed: `export default function Component()` / `export default Comp;`
3. **Severity matrix** (`_compute_exit_code`):
   - !ci, * → exit 0
   - ci, 0 findings → exit 0
   - ci, >0 findings → exit 1
4. **Robustness**: empty source / weird whitespace / multi-rule on one line
5. **Live dogfood** (`TestLiveRepo`): real `docs/interactive/**/*.jsx` → 0
   findings (S#92 fix already merged; ship strict-from-day-1).

Generated by ``scripts/tools/dx/scaffold_lint.py``. See PR #171.
"""
from __future__ import annotations

import os
import sys
from pathlib import Path

import pytest

_TOOLS_DIR = os.path.join(
    os.path.dirname(__file__), "..", "..", "scripts", "tools", "lint"
)
sys.path.insert(0, _TOOLS_DIR)

import check_jsx_loader_compat as lint  # noqa: E402


def _scan(source: str, fake_path: str = "fake.jsx"):
    return lint.scan_source(Path(fake_path), source)


# ---------------------------------------------------------------------------
# Detection — named-export rule
# ---------------------------------------------------------------------------
class TestNamedExportRule:
    @pytest.mark.parametrize(
        "src",
        [
            "export function computeTrace() {}\n",
            "export const HELPER = 42;\n",
            "export let counter = 0;\n",
            "export var legacy = 1;\n",
            "export class MyClass {}\n",
            "  export function indented() {}\n",  # leading whitespace
        ],
    )
    def test_named_export_flagged(self, src):
        findings = _scan(src)
        assert len(findings) == 1
        assert findings[0].rule == "named-export"

    @pytest.mark.parametrize(
        "src",
        [
            "export default function Component() {}\n",
            "export default Component;\n",
            "export default function() {}\n",  # anon default
            "export default class Foo {}\n",
            "  export default function Indented() {}\n",
        ],
    )
    def test_export_default_allowed(self, src):
        assert _scan(src) == []

    def test_module_scope_helper_no_export_allowed(self):
        # Common pattern in cicd-setup-wizard / deployment-wizard /
        # alert-builder / routing-trace: helpers at module scope without
        # `export` keyword. Component closure reaches them.
        src = (
            "function helper() { return 42; }\n"
            "const CONSTANTS = { a: 1 };\n"
            "export default function MyTool() {}\n"
        )
        assert _scan(src) == []


