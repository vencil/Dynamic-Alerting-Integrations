class TestMain:
    @pytest.mark.timeout(15)
    def test_main_clean_exit_0(self, tmp_path, capsys):
        clean = tmp_path / "clean.go"
        clean.write_text(
            'package x\nvar q = `rate(m{tenant="$tenant"}[5m])`\n',
            encoding="utf-8",
        )
        rc = lint.main(["--ci", str(clean)])
        assert rc == 0

    @pytest.mark.timeout(15)
    def test_main_dirty_under_ci_exit_1(self, tmp_path, capsys):
        dirty = tmp_path / "dirty.go"
        dirty.write_text(
            'package x\nvar q = `rate(m{tenant="db-a"}[5m])`\n',
            encoding="utf-8",
        )
        rc = lint.main(["--ci", str(dirty)])
        assert rc == 1
        captured = capsys.readouterr()
        assert "1 finding" in captured.err
        assert "db-a" in captured.err

    @pytest.mark.timeout(15)
    def test_main_dirty_no_ci_exit_0(self, tmp_path, capsys):
        dirty = tmp_path / "dirty.go"
        dirty.write_text(
            'package x\nvar q = `m{tenant="db-a"}`\n',
            encoding="utf-8",
        )
        rc = lint.main([str(dirty)])
        assert rc == 0  # report-only mode


# ---------------------------------------------------------------------------
# Live dogfood — actual repo must pass
# ---------------------------------------------------------------------------
class TestLiveRepo:
    """Run the lint against the actual repo. If this PR's edits don't
    fully eliminate findings, this test fails — preventing PR from
    landing in a broken state.
    """

    @pytest.mark.timeout(60)
    def test_live_repo_has_no_findings(self):
        import argparse

        ns = argparse.Namespace(paths=[])
        candidates = lint._resolve_target_paths(ns)
        assert candidates, "expected production code roots to exist"

        all_findings = []
        for path in candidates:
            try:
                source = path.read_text(encoding="utf-8", errors="replace")
            except OSError:
                continue
            all_findings.extend(lint.scan_source(path, source))

        assert all_findings == [], (
            f"Live repo has {len(all_findings)} finding(s):\n"
            + "\n".join(f"  - {f.render()}" for f in all_findings[:10])
        )
